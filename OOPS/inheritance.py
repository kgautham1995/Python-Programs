# Inheritance:Inheritance is the process of getting
# properties from one class to another class
# The class which is providing the variables and methods(properties)
# is called as the super class
# the class taking or accepting the variables and methods from the
# super class is called as the sub class or child class or derived class
# in inheritance everything is inherited in the sub class except
# the private properties
# in python we have 5 types of inheritance
# Single inheritance
# multi level inheritance
# Multiple inheritance
# Hierarchical inheritance
# Hybrid inheritance