# Object Oriented Programming structure in python(OOPS)
# Abstraction:Abstraction is the process of gathering
# required data and hiding unnecessary data
# Encapsulation:Encapsulation is a process of wrapping related
# state and behaviour into a single container
# State means Variable which holds data temporarily
# Behaviour means method which will perform certain operations on the data
# Inheritance:Inheritance is the process of getting
# properties from one class to another class
# Polimorphism: Poly means many and Morphs means forms
